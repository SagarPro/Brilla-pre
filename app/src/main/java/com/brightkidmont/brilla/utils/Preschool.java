package com.brightkidmont.brilla.utils;

public class Preschool {

    public String preschoolName;
    public String preschoolPhone;
    public String preschoolLogin;
    public String preschoolPassword;
    public String preschoolLocation;

    public Preschool() {
    }

    public Preschool(String preschoolName, String preschoolPhone, String preschoolLogin, String preschoolPassword, String preschoolLocation) {
        this.preschoolName = preschoolName;
        this.preschoolPhone = preschoolPhone;
        this.preschoolLogin = preschoolLogin;
        this.preschoolPassword = preschoolPassword;
        this.preschoolLocation = preschoolLocation;
    }

}
